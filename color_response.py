# Step 1: Input from user
response = input("Enter your favorite color: ").lower()

# Step 2: Use if-elif-else to respond
if response == "red":
    print("Red is the color of passion!")
elif response == "blue":
    print("Blue is calm and peaceful.")
elif response == "green":
    print("Green represents nature and growth.")
else:
    print("That's a unique choice!")
